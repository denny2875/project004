public class Quzi6 {

	public static void main(String[] args) {
		
		int score = 90;
		
		if ((90 <= score) && (99 >= score)) {
			System.out.println("수");
		}else if ((80 <= score) && (89 >= score)) {
			System.out.println("우");
		}else if ((70 <= score) && (79 >= score)){
			System.out.println("미");
		}else
			System.out.println("양");
		
		
		
		
	}

}
